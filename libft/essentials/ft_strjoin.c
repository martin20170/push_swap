#include "essentials.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	int		i;
	int		final_len;

	i = 0;
	final_len = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	result = malloc(sizeof(char) * (final_len + 1));
	if (!result)
		return (NULL);
	while (*s1)
	{
		result[i] = *s1;
		s1++;
		i++;
	}
	while (*s2)
	{
		result[i] = *s2;
		s2++;
		i++;
	}
	result[i] = '\0';
	return (result);
}
