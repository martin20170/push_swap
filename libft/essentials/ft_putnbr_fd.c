#include "essentials.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	mod;
	char	a;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		mod = n % 10;
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		a = '0' + mod;
		write(fd, &a, 1);
	}
}
